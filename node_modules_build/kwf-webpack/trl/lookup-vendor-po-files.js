'use strict';
const glob = require("glob");
const path = require("path");

module.exports = function(webPath = ".")
{
    let ret = [];
    glob.sync(path.resolve(process.cwd(), `${webPath}/vendor`) + '/*/*/trl/en.po').forEach(i => { //in vendor code language is en, thus en.po must always exist
        ret.push(i.replace(/\/en\.po$/, '/[language].po'));
    });
    return ret;
};
